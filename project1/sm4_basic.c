// 字节替换函数
static uint32_t S(uint32_t x) {
    return (Sbox[(x >> 24) & 0xFF] << 24) |
           (Sbox[(x >> 16) & 0xFF] << 16) |
           (Sbox[(x >> 8) & 0xFF] << 8) |
           Sbox[x & 0xFF];
}

// 线性变换L
static uint32_t L(uint32_t x) {
    return x ^ (x << 2) ^ (x << 10) ^ (x << 18) ^ (x << 24) ^
           ((x >> 30) & 3) ^ ((x >> 22) & 0x300) ^ 
           ((x >> 14) & 0x30000) ^ ((x >> 6) & 0x3000000);
}

// 合成置换函数T
static uint32_t T(uint32_t x) {
    return L(S(x));
}

// 密钥扩展函数
static void sm4_key_expansion(const uint8_t *key, uint32_t rk[32]) {
    uint32_t MK[4];
    uint32_t K[36];
    int i;
    
    // 加载密钥
    MK[0] = (key[0] << 24) | (key[1] << 16) | (key[2] << 8) | key[3];
    MK[1] = (key[4] << 24) | (key[5] << 16) | (key[6] << 8) | key[7];
    MK[2] = (key[8] << 24) | (key[9] << 16) | (key[10] << 8) | key[11];
    MK[3] = (key[12] << 24) | (key[13] << 16) | (key[14] << 8) | key[15];
    
    // 初始化密钥
    K[0] = MK[0] ^ FK[0];
    K[1] = MK[1] ^ FK[1];
    K[2] = MK[2] ^ FK[2];
    K[3] = MK[3] ^ FK[3];
    
    // 生成轮密钥
    for (i = 0; i < 32; i++) {
        K[i+4] = K[i] ^ T(K[i+1] ^ K[i+2] ^ K[i+3] ^ CK[i]);
        rk[i] = K[i+4];
    }
}

// SM4加密函数
void sm4_encrypt(const uint8_t *key, const uint8_t *input, uint8_t *output) {
    uint32_t rk[32];
    uint32_t X[36];
    int i;
    
    // 密钥扩展
    sm4_key_expansion(key, rk);
    
    // 加载明文
    X[0] = (input[0] << 24) | (input[1] << 16) | (input[2] << 8) | input[3];
    X[1] = (input[4] << 24) | (input[5] << 16) | (input[6] << 8) | input[7];
    X[2] = (input[8] << 24) | (input[9] << 16) | (input[10] << 8) | input[11];
    X[3] = (input[12] << 24) | (input[13] << 16) | (input[14] << 8) | input[15];
    
    // 32轮迭代
    for (i = 0; i < 32; i++) {
        X[i+4] = X[i] ^ T(X[i+1] ^ X[i+2] ^ X[i+3] ^ rk[i]);
    }
    
    // 输出密文
    output[0] = (X[35] >> 24) & 0xFF;
    output[1] = (X[35] >> 16) & 0xFF;
    output[2] = (X[35] >> 8) & 0xFF;
    output[3] = X[35] & 0xFF;
    output[4] = (X[34] >> 24) & 0xFF;
    output[5] = (X[34] >> 16) & 0xFF;
    output[6] = (X[34] >> 8) & 0xFF;
    output[7] = X[34] & 0xFF;
    output[8] = (X[33] >> 24) & 0xFF;
    output[9] = (X[33] >> 16) & 0xFF;
    output[10] = (X[33] >> 8) & 0xFF;
    output[11] = X[33] & 0xFF;
    output[12] = (X[32] >> 24) & 0xFF;
    output[13] = (X[32] >> 16) & 0xFF;
    output[14] = (X[32] >> 8) & 0xFF;
    output[15] = X[32] & 0xFF;
}

// SM4解密函数（使用逆序轮密钥）
void sm4_decrypt(const uint8_t *key, const uint8_t *input, uint8_t *output) {
    uint32_t rk[32];
    uint32_t rk_rev[32];
    uint32_t X[36];
    int i;
    
    // 密钥扩展
    sm4_key_expansion(key, rk);
    
    // 解密时使用逆序轮密钥
    for (i = 0; i < 32; i++) {
        rk_rev[i] = rk[31 - i];
    }
    
    // 加载密文
    X[0] = (input[0] << 24) | (input[1] << 16) | (input[2] << 8) | input[3];
    X[1] = (input[4] << 24) | (input[5] << 16) | (input[6] << 8) | input[7];
    X[2] = (input[8] << 24) | (input[9] << 16) | (input[10] << 8) | input[11];
    X[3] = (input[12] << 24) | (input[13] << 16) | (input[14] << 8) | input[15];
    
    // 32轮迭代
    for (i = 0; i < 32; i++) {
        X[i+4] = X[i] ^ T(X[i+1] ^ X[i+2] ^ X[i+3] ^ rk_rev[i]);
    }
    
    // 输出明文
    output[0] = (X[35] >> 24) & 0xFF;
    output[1] = (X[35] >> 16) & 0xFF;
    output[2] = (X[35] >> 8) & 0xFF;
    output[3] = X[35] & 0xFF;
    output[4] = (X[34] >> 24) & 0xFF;
    output[5] = (X[34] >> 16) & 0xFF;
    output[6] = (X[34] >> 8) & 0xFF;
    output[7] = X[34] & 0xFF;
    output[8] = (X[33] >> 24) & 0xFF;
    output[9] = (X[33] >> 16) & 0xFF;
    output[10] = (X[33] >> 8) & 0xFF;
    output[11] = X[33] & 0xFF;
    output[12] = (X[32] >> 24) & 0xFF;
    output[13] = (X[32] >> 16) & 0xFF;
    output[14] = (X[32] >> 8) & 0xFF;
    output[15] = X[32] & 0xFF;
}
